name = 'Bruce Wayne'

# find the length of string
print(len(name))

# convert to upper case
print(name.upper())

# lower case
print(name.lower())
print(name.title())

# find the index of one or more charcater
print(name.find('u')) 

#replace one or more chart
print(name.replace('Br', 'Tr'))

# check the existence of one more char in strings
print('Bruce' in name) # a boolean expression
print('Truce' in name)
