# defining a function with parameter
def greet_user(name):
  print(f'Hello {name}')
  print("Welcome to the new world!")

# calling a function with argument
greet_user("John")
greet_user("Mary")

# function defined w multiple parameters
def print_name (first_name, last_name):
  print(f'My name is {first_name} {last_name}...')

# function called w multiple arguments
print_name('Durgesh', 'Chaudhary')
print_name('Sidhant', 'Gumber')